                        <h2><u>About Us</u></h2>
                        <p>Abound is a CCIC-SJ English ministry for young adults (college and young professionals) who want to live and love like Jesus through the power of the gospel. </p>
                        <p>Our name comes from Philippians 1:9-11 (ESV): "And it is my prayer that your love may <em>abound</em> more and more, with knowledge and all discernment, so that you may approve what is excellent, and so be pure and blameless for the day of Christ, filled with the fruit of righteousness that comes through Jesus Christ, to the glory and praise of God." College and post-college years can be difficult times, with transitions, identity, relationships, and purpose. Our hope is to grow in accordance to the will of God so as to be equipped to live the rest of our lives in a way that pleases God. We strive to regularly do this through the word, prayer, worship, and accountability in a Christ-centered community.</p>
                        <p>Abound meets every Wednesday, 7:30PM in the Fireside Room on the 2nd floor of CCIC-SJ. For more information, check out Abound CCIC San Jose on Facebook or contact Pastor Eric Maxwell at <a href="mailto:esmaxwell@gmail.com">esmaxwell@gmail.com</a>.</p>
